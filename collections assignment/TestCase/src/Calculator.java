
public class Calculator {
public int Add(int a,int b) {
return a+b;
}

public float Add(float c,float d) {
return c+d;

}
public double Add(double e ,double f) {
return e+f;

}
public int Sub(int g ,int h) {
return g-h;

}
public float Sub(float i ,float j) {
return i-j;

}
public double Sub(double k ,double l) {
return k-l;

}

public int Mul(int g ,int h) {
return g*h;

}
public float Mul(float i ,float j) {
return i*j;

}
public double Mul(double k ,double l) {
return k*l;

}
public int Div(int g ,int h) {
return g/h;

}
public float Div(float i ,float j) {
return i/j;

}
public double Div(double k ,double l) {
return k/l;

}



}
